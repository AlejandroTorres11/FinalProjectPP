    @Override
    public void run() {
        //System.out.println("--------------------------------COMIENZA EL RUNEO-------------------------------------");
        while (true) {
            try {
                update();
                Thread.sleep(100); // Pausa para evitar saturar la memoria
            } catch (InterruptedException e) {
                e.printStackTrace();
            } catch (ConnectException ce) {
                // Mostrar el mensaje de error
                JOptionPane.showMessageDialog(null, 
                    "Se ha desconectado el servidor. Intente nuevamente más tarde.", 
                    "Error de Conexión", 
                    JOptionPane.ERROR_MESSAGE);

                // Cerrar la ventana (dispose)
                dispose();

                // Finalizar el programa (si es necesario)
                System.exit(0);

                ce.printStackTrace();
            } catch (RemoteException ex) {
                Logger.getLogger(ClienteUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
